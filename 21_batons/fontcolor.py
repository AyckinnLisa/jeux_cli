# ----- FONT COLOR MODULE -----

def red_font(txt):
    return f"\033[91m{txt}\033[0m"

def green_font(txt):
    return f"\033[92m{txt}\033[0m"

def yellow_font(txt):
    return f"\033[93m{txt}\033[0m"

def magenta_font(txt):
    return f"\033[95m{txt}\033[0m"

def blue_font(txt):
    return f"\033[94m{txt}\033[0m"
